create or replace function public.spend_user_points(
  p_user_id uuid,
  p_amount integer,
  p_transaction_type text default 'consume',
  p_reason text default '',
  p_metadata jsonb default '{}'::jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_row public.user_points_balances;
  v_previous_balance integer;
  v_transaction public.user_points_transactions;
begin
  if p_user_id is null then
    raise exception 'user_id 不能为空';
  end if;
  if p_amount is null or p_amount < 0 then
    raise exception 'amount 不能为空';
  end if;

  perform public.ensure_user_points_balance(p_user_id);

  select balance into v_previous_balance
  from public.user_points_balances
  where user_id = p_user_id;

  update public.user_points_balances
  set balance = balance - p_amount,
      total_spent = total_spent + p_amount
  where user_id = p_user_id
    and balance >= p_amount
  returning * into v_row;

  if found then
    v_transaction := public.insert_user_points_transaction(
      p_user_id,
      -p_amount,
      coalesce(v_previous_balance, 0),
      v_row.balance,
      coalesce(nullif(trim(p_transaction_type), ''), 'consume'),
      coalesce(p_reason, ''),
      coalesce(p_metadata, '{}'::jsonb),
      'api',
      null
    );

    return jsonb_build_object(
      'success', true,
      'spent', true,
      'balance_row', to_jsonb(v_row),
      'transaction_row', to_jsonb(v_transaction)
    );
  end if;

  raise exception '积分余额不足';
end;
$$;

